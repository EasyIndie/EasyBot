    #[test]
    fn test_generate_service_files_returns_platform_files() {
        let paths = test_paths();
        let files = generate_service_files(&paths);

        if cfg!(target_os = "linux") {
            assert!(files.iter().any(|(p, _)| p.ends_with("easybot.service")));
            assert!(files.iter().any(|(p, _)| p.ends_with("easybot.sh")));
            assert_eq!(files.len(), 2);
        } else if cfg!(target_os = "macos") {
            assert!(
                files
                    .iter()
                    .any(|(p, _)| p.ends_with("com.easybot.gateway.plist"))
            );
            assert!(files.iter().any(|(p, _)| p.ends_with("easybot.sh")));
            assert_eq!(files.len(), 2);
        } else if cfg!(target_os = "windows") {
            assert!(files.iter().any(|(p, _)| p.ends_with("manage-service.ps1")));
            assert_eq!(files.len(), 1);
        } else {
            assert_eq!(files.len(), 0);
        }
    }
